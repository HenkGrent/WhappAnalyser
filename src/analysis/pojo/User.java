package analysis.pojo;

/**
 * POJO which represents a person who participated (i.e. did anything) in a Whatsapp Chat.
 * @author Henk
 *
 */
public class User {
	
	/**
	 * Represents the name of a user (e.g. "John").
	 */
	private String name;
	
	public User(final String name) {
		this.name = name;
	}
}
